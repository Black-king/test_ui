from PIL import Image, ImageDraw
import os
import math

# 创建图标目录
def ensure_icon_dir():
    icon_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'icons')
    if not os.path.exists(icon_dir):
        os.makedirs(icon_dir)
    return icon_dir

# 创建终端图标
def create_terminal_icon():
    img = Image.new('RGBA', (48, 48), color=(0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    
    # 绘制一个蓝色的圆形背景
    draw.ellipse((4, 4, 44, 44), fill=(52, 152, 219, 255))
    
    # 绘制终端图标
    draw.rectangle((14, 14, 34, 34), fill=(255, 255, 255, 255))
    draw.line((18, 22, 24, 22), fill=(52, 152, 219, 255), width=2)
    draw.line((18, 26, 30, 26), fill=(52, 152, 219, 255), width=2)
    draw.line((18, 30, 27, 30), fill=(52, 152, 219, 255), width=2)
    
    # 保存为ICO和SVG文件
    icon_dir = ensure_icon_dir()
    img.save(os.path.join(icon_dir, 'terminal.ico'), format='ICO', sizes=[(16, 16), (32, 32), (48, 48)])
    
    # 创建SVG内容
    svg_content = '''
    <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-terminal" viewBox="0 0 16 16">
      <path d="M6 9a.5.5 0 0 1 .5-.5h3a.5.5 0 0 1 0 1h-3A.5.5 0 0 1 6 9zM3.854 4.146a.5.5 0 1 0-.708.708L4.793 6.5 3.146 8.146a.5.5 0 1 0 .708.708l2-2a.5.5 0 0 0 0-.708l-2-2z"/>
      <path d="M2 1a2 2 0 0 0-2 2v10a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V3a2 2 0 0 0-2-2H2zm12 1a1 1 0 0 1 1 1v10a1 1 0 0 1-1 1H2a1 1 0 0 1-1-1V3a1 1 0 0 1 1-1h12z"/>
    </svg>
    '''
    
    with open(os.path.join(icon_dir, 'terminal.svg'), 'w') as f:
        f.write(svg_content)

